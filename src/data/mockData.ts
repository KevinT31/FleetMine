function seedFromId(id: string): number {
  return id.split('').reduce((sum, char) => sum + char.charCodeAt(0), 0);
}

function createTelemetrySeries(vehicle: Vehicle, points = 336): TelemetryPoint[] {
  const base = typeBaseMap[vehicle.type];
  const seed = seedFromId(vehicle.id);

  return Array.from({ length: points }).map((_, idx) => {
    const distance = points - idx;
    const timestamp = new Date(now - distance * 30 * 60_000).toISOString();
    const wave = Math.sin((idx + seed) / 9) * 1.6;
    const waveSlow = Math.cos((idx + seed) / 18) * 1.2;
    const statusShift =
      vehicle.status === 'critical'
        ? 5.8
        : vehicle.status === 'warning'
          ? 2.7
          : vehicle.status === 'offline'
            ? -4.8
            : 0;

    return {
      ts: timestamp,
      tempC: base.temp + statusShift + wave + waveSlow,
      pressureBar: base.pressure + statusShift * 1.1 + waveSlow * 2.4,
      vibrationMm_sRms: Math.max(0.1, base.vibration + statusShift * 0.42 + wave * 0.35),
      speedKmh: vehicle.status === 'offline' ? 0 : Math.max(0, base.speed + wave * 1.9 + waveSlow * 1.2),
    };
  });
}

